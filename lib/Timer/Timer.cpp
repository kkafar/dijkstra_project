////////////////////////////////////////////////////////////
#include "Timer.hpp"
////////////////////////////////////////////////////////////

////////////////////////////////////////////////////////////
void Timer::Start()
{
    start = std::chrono::high_resolution_clock::now();
}
////////////////////////////////////////////////////////////
void Timer::Stop()
{
    end = std::chrono::high_resolution_clock::now();
    elapsed = end - start;
}
////////////////////////////////////////////////////////////
double Timer::GetElapsedTime() const
{
    return elapsed.count();
}
